    El administrador de los  centros deportivos tiene la potestad de cancelar una reserva en casos de fuerza  mayor, caso en el cual, el administrador se comunicará directamente con el  usuario para coordinar un nuevo horario de reserva y/o devolución íntegra del  dinero depositado.</p>
  <p><strong>Artículo 4.</strong><br />
    Los usuarios registrados  podrán realizar sus reservas desde la página web, las cuales aparecerán como “pendientes  de pago” o “pendientes de validación”. Una vez realizado el pago establecido,  el centro deportivo emitirá una confirmación vía correo electrónico aprobando  la reserva efectuada. En caso de una cancelación dentro del plazo establecido  durante la reserva*, el usuario deberá comunicarse directamente con el centro  deportivo para la aprobación de la misma. <br />
